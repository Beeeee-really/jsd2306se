package string;

/**
 * boolean startsWith(String str)
 * <p>判断当前字符串是否以str开头
 * <p></p>boolean endsWith(String str)
 * <p>判断当前字符串是否以str结尾
 */
public class StartWithDemo {
    public static void main(String[] args) {
        System.out.println("qwq qwq qwq qwq".startsWith("qwq"));
        System.out.println("qwq qwq qwq qwq".endsWith("awa"));
    }
}
